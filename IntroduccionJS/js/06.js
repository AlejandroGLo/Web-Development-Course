// Math Object

let result;

result = Math.PI; //Returns the value of PI
result = Math.round(2.4); //Rounds the number
result = Math.ceil(2.4); //Rounds the number up
result = Math.floor(2.9); //Rounds the number down
result = Math.sqrt(144); //Returns the square root
result = Math.abs(-200); //Returns the absolute value
result = Math.min(3, 5, 1, 12, -3); //Returns the minimum value
result = Math.max(3, 5, 1, 12, -3); //Returns the maximum value
result = Math.random(); //Returns a random number
result = Math.floor(Math.random() * 30); //Returns a random number between 0 and 30


console.log(result);